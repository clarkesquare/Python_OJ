n = int(input())

# 위
print("*" * n + " " * ((n-2)*2+1) + "*" * n )
for i in range(1, n-1):
    print(" " * i + "*" + " " * (n-2) + "*" + " " * (((n-i-2)*2+1)) + "*" + " " * (n-2) + "*")

# 중앙
print(" " * (n-1) + "*" + " " * (n-2) + "*" + " " * (n-2) + "*")

# 아래
for i in range(n-2, 0, -1):
    print(" " * i + "*" + " " * (n-2) + "*" + " " * (((n-i-2)*2+1)) + "*" + " " * (n-2) + "*")

print("*" * n + " " * ((n-2)*2+1) + "*" * n)